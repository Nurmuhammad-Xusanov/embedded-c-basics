#include <stdio.h>

int main(void) {
  int a = 5; // 0101
  int b = 3; // 0011

  int c = a | b;
  printf("%d\n", c);

  int x = 0; 
  x = x | 1; // 00000001 -> 1 (1 << 0)
  x = x | (1 << 1); // 00000010 -> 2
  x = x | (1 << 2); // 00000100 -> 4
  x = x | (1 << 3); // 00001000 -> 8
  x = x | (1 << 4); // 00010000 -> 16
  x = x | (1 << 5); // 00100000 -> 32
  x = x | (1 << 6); // 01000000 -> 64
  x = x | (1 << 7); // 10000000 -> 128
  printf("%d\n", x);
  return 0;
}